let base
let height

function areaOfTriangle(){
    1/2 * base * height
}

let side

function areaOfSquare(){
    side * side
}

let pi = 3.142
let radius

function areaOfCircle(){
    pi * radius * radius
}

let length
let width

function areaOfQuadrilateral(){
    length * width
}